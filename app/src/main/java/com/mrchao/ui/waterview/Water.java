package com.mrchao.ui.waterview;

/**
 */

public class Water {
    private int number;
    private String name;

    public Water(int number, String name) {
        this.number = number;
        this.name = name;
    }

    public int getNumber() {
        return number;
    }

    public String getName() {
        return name;
    }
}
